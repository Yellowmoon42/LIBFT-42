#include "libft.h"

char	*ft_strmapi(char const *str, char (*f)(unsigned int, char))
{
	char			*ptr;
	unsigned int	i;

	if (!str || !f)
		return (0);
	ptr = (char *)malloc(ft_strlen(str) + 1);
	if (!ptr)
		return (0);
	i = 0;
	while (str[i])
	{
		ptr[i] = f(i, str[i]);
		i++;
	}
	ptr[i] = '\0';
	return (ptr);
}
